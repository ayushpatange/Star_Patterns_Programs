package java_patterns;

public class Square_Pattern {

	public static void main(String[] args) {
	
//		*******
//		*******
//		*******
//		*******
		
		int num=4;
		
		for(int i=1;i<=num;i++) {
			for(int j=1;j<=7;j++) {
				System.out.print("*");
			}
			System.out.println("");
		}

	}

}
