package java_patterns;

public class Rectangle_Pattern {

	public static void main(String[] args) {
		
//		Rectangle Pattern
//
//		***********
//		***********
//		***********
		
		System.out.println("Rectangle Pattern");
		System.out.println();
		
		int num=3;
		for(int i=0;i<num;i++) {
			for(int j=1;j<=11;j++) {
				System.out.print("*");
			}
			System.out.println("");
		}

	}

}
